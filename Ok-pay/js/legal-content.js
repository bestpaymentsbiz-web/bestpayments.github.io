function openLegalModal(type) {
  var data = LEGAL_CONTENT[type];
  if (!data) return;
  document.getElementById('legalModalTitle').textContent = data.title;
  document.getElementById('legalModalBody').innerHTML = data.html;
  var overlay = document.getElementById('legalModalOverlay');
  overlay.classList.add('open');
  document.body.style.overflow = 'hidden';
  overlay.querySelector('.legal-modal-body').scrollTop = 0;
}

function closeLegalModal() {
  var overlay = document.getElementById('legalModalOverlay');
  overlay.classList.remove('open');
  document.body.style.overflow = '';
}

document.addEventListener('keydown', function (e) {
  if (e.key === 'Escape') closeLegalModal();
});
